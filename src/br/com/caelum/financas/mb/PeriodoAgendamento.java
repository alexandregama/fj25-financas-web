package br.com.caelum.financas.mb;

public class PeriodoAgendamento {

	private String expressaoEmMinutos;
	
	private String expressaoEmSegundos;

	public String getExpressaoEmMinutos() {
		return expressaoEmMinutos;
	}

	public void setExpressaoEmMinutos(String expressaoEmMinutos) {
		this.expressaoEmMinutos = expressaoEmMinutos;
	}

	public String getExpressaoEmSegundos() {
		return expressaoEmSegundos;
	}

	public void setExpressaoEmSegundos(String expressaoEmSegundos) {
		this.expressaoEmSegundos = expressaoEmSegundos;
	}
	
}
